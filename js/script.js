//sidenav

function openNav() {
    document.getElementById("mySidenav").style.cssText = "width: 33.333%";
}

function closeNav() {
    document.getElementById("mySidenav").style.cssText = "width: 0";
}

function openNavPortrait() {
    document.getElementById("mySidenav").style.cssText = "height: 100%";
}

function closeNavPortrait() {
    document.getElementById("mySidenav").style.cssText = "height: 0";
}